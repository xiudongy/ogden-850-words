// Helper: get all words as a flat array
function getAllWords() {
  return WORD_DATA.categories.flatMap(cat =>
    cat.words.map(w => ({ ...w, category: cat.id, categoryName: cat.nameZh, categoryIcon: cat.icon, categoryColor: cat.color }))
  );
}

// Helper: get words by category
function getWordsByCategory(categoryId) {
  const cat = WORD_DATA.categories.find(c => c.id === categoryId);
  if (!cat) return [];
  return cat.words.map(w => ({ ...w, category: cat.id, categoryName: cat.nameZh, categoryIcon: cat.icon, categoryColor: cat.color }));
}

// Helper: get random words (excluding certain words) - uses proper Fisher-Yates shuffle
function getRandomWords(count, exclude = []) {
  const all = getAllWords().filter(w => !exclude.includes(w.en));
  return shuffle(all).slice(0, count);
}

// Helper: shuffle array
function shuffle(arr) {
  const a = [...arr];
  for (let i = a.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [a[i], a[j]] = [a[j], a[i]];
  }
  return a;
}
